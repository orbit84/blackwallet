		    <h5 class="blue-text">What are Lumens?</h5>
		    <blockquote>
			    <p>Lumens are the native asset of the Stellar network.</p>
				<p>Native means that lumens are built into the network. Asset is how the network refers to an item of value that is stored on the ledger.</p>

				<p>One lumen is a unit of digital currency, like a bitcoin.<br/>

					While you can’t hold a lumen in your hand, they are essential to the Stellar network—they contribute to the ability to move money around the world and to conduct<br/> transactions between different currencies quickly and securely.
				</p>
				<p class="grey-text">from <a target="_blank" href="https://www.stellar.org/lumens/">stellar.org</a></p>
			</blockquote>
