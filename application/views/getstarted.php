		    <h5 class="blue-text">How do I receive Lumens?</h5>
		    <blockquote>
			    <p>
			    	First, you need a wallet for this. Guess what? That's exactly what BlackWallet is!<br/>
			    	Then, you have to generate a key pair (a public and a private key), these are used to access to your wallet.
			    </p>
			    <p>The public key starts with a <b>G</b> while the secret key starts with a <b>S</b></p>
			    <p class="red-text text-lighten-3">
			    	You must absolutely keep secret your private key (which is actually called secret key), if someone else has access to it, he has access to your funds!
			    </p>
			    <p>
			    	To receive Lumens, you have to give your <u>public key</u> to the sender.
			    </p>
			</blockquote>
		  </div>
