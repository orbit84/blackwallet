		    <h5 class="blue-text">How do I use BlackWallet as an app-like?</h5>
		    <blockquote>
			      	  <p>BlackWallet can be used on any device as long as the device can execute javascript code.</p>
				      <p>Thanks to the LocalStorage technology, you can use BlackWallet as a mobile / tablet application.<br/>To do so, follow these simple steps: </p>
				      <ol>
				      	<li> Browse BlackWallet using your device</li>
				      	<li> Unlock your wallet using your private key</li>
				      	<li> Go to the settings <i class="fa fa-cog"></i> and enable Mobile mode</li>
				      	<li> Finished!</li>
				      </ol>
				      <p>Now, each time you will browse the page through your bookmarks or screenboard, your wallet will automatically be unlocked.<br/>This mode can only disabled by logging out <i class="fa fa-power-off red-text"></i>.</p>
		   	</blockquote>
		  </div>
		  <div class="section">
		    <h5 class="blue-text">How can I have multiple accounts?</h5>
		    <blockquote>
			      	  <p>BlackWallet can be used to manage multiple accounts.<br/>It's pretty easy to set up:</p>
				      <ol>
				      	<li> Enable the Mobile mode in the settings</li>
				      	<li> In the accounts directory, click the <i class="fa fa-cog"></i></li>
				      	<li> You can now manage your accounts (add new, rename, delete etc.)</li>
				      </ol>
			      	  <p>To switch from one account to another, simply click on the account you'd like to use.</p>
		   	</blockquote>
		  </div>
		  <div class="section">
		    <h5 class="blue-text">How to merge accounts?</h5>
		    <blockquote>
			      	  <p>Access the account you would like to send all your funds from, then:</p>
				      <ol>
				      	<li> Click "send money" (<i class="fa fa-send"></i>)</li>
				      	<li> To: enter the account's id you would like to send your funds to</li>
				      	<li> Amount: enter a number greater or equal your XLM balance</li>
				      	<li> Click send</li>
				      </ol>
			      	  <p>A popup will appear and prompt if you'd like to send all your Lumens to the address, click yes.</p>
			      	  <p class="red-text text-lighten-3">You can't merge your account if you have assets or active trust lines - make sure to empty your assets and delete your trust lines if you encounter errors.</p>
		   	</blockquote>
		  </div>

		  <div class="section">
		    <h5 class="blue-text">How to add/remove an asset trust line?</h5>
		    <blockquote>
			      	  <p>Access to your account, then:</p>
				      <ol>
				      	<li> Click "holdings" (<i class="fa fa-suitcase"></i>)</li>
				      	<li> Go to the "TRUST" menu</li>
				      	<li> Asset code: enter the asset code (usually 3 or 4 letters long, like XLM, BTC...)</li>
				      	<li> Asset issuer: enter the asset's issuer account</li>
				      	<li> Limit: set to 0 to remove the trust line, do not enter anything to use the max limit.</li>
				      	<li> Click "UPDATE"</li>
				      </ol>
			      	  <p>Once the trust line was updated, you can go back to the "ASSETS" menu, if you added a trust line, the asset
			      	  should be displayed in the list - if you removed the trust line, the asset should not be listed anymore.</p>
		   	</blockquote>
		  </div>

		  <div class="section">
		    <h5 class="blue-text">How to set the inflation destination address?</h5>
		    <blockquote>
			      	  <p>Access to your account, then:</p>
				      <ol>
				      	<li> Click "settings" (<i class="fa fa-cog"></i>)</li>
				      	<li> Go to the "MONEY" menu</li>
				      	<li> Inflation destination: enter the destination address</li>
				      	<li> Click "SET"</li>
				      </ol>
		   	</blockquote>
		  </div>

		  <div class="section">
		    <h5 class="blue-text">How to send Lumens to an email? <i class="fa fa-at"></i> </h5>
		    <blockquote>
			      	  <p>Pretty easy with blackwallet, just send Lumens like if you were sending to a public address - but instead - enter the email address in the destination field.<br/>
			      	  Blackwallet use an escrow account system for the mail sending feature, you will be displayed the secret key of that escrow account: <b>save it</b>, you can use it to refund yourself in case your recipient never claims the lumens.</p>
		   	</blockquote>
		  </div>


		  <div class="section">
		    <h5 class="blue-text">How can I get a federated name?</h5>
		    <blockquote>
			      	  <p>Blackwallet has an open federation service for you! To get a federated name, access to your account and then:</p>
				      <ol>
				      	<li> Click "receive money"</li>
				      	<li> Click the <i class="fa fa-user"></i> button</li>
				      	<li> Enter the name you want</li>
				      	<li> Click "SET"</li>
				      </ol>
			      	  <p>Congrats! People can now send you lumens at <b>yourname*blackwallet.co</b> on any wallet that supports federated names.</p>
		   	</blockquote>
		  </div>

		  <div class="section">
		    <h5 class="blue-text">How can I link my email to my account?</h5>
		    <blockquote>
			      	  <p>Blackwallet uses a secret hash system to prevent users from linking emails they do not own to an account id.<br/>
			      	  For that reason, the only way to link your email to an account id is to send Lumens to your email address using blackwallet.
			      	  </p>
				      <ol>
				      	<li> Send Lumens to your email</li>
				      	<li> Click the claim button in the mail</li>
				      	<li> Click "send funds to a new account"</li>
				      	<li> Check "yes, link my email"</li>
				      	<li> Click "CLAIM"</li>
				      </ol>
			      	  <p>Now each time someone will send you Lumens to <b>youremail@domain.com</b>, the lumens will be sent directly to the account linked to your email.</p>
		   	</blockquote>
